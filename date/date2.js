var date = new Date(); //creation of date object
console.log(date); //printing the date object
date.setFullYear(2007); //setting the year
date.setMonth(6); //setting the month
date.setDate(7); //setting the date
date.setHours(18); //setting the hours
date.setMinutes(0); //setting the minutes
date.setSeconds(0); //setting the seconds
date.setMilliseconds(0); //setting the milliseconds


console.log(date); //printing the date object
